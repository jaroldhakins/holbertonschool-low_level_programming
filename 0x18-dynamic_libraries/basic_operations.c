#include <stdio.h>
/**
 * add - add two numbers
 * @a: number one
 * @b: number two
 * Return: a + b
 */
int add(int a, int b)
{
    return a + b;
}
/**
 * sub - add two numbers
 * @a: number one
 * @b: number two
 * Return: a + b
 */
int sub(int a, int b)
{
    return a - b;
}
/**
 * mul - add two numbers
 * @a: number one
 * @b: number two
 * Return: a + b
 */
int mul(int a, int b)
{
    return a * b;
}
/**
 * div - add two numbers
 * @a: number one
 * @b: number two
 * Return: a + b
 */
int div(int a, int b)
{
    return a / b;
}
/**
 * mod - add two numbers
 * @a: number one
 * @b: number two
 * Return: a + b
 */
int mod(int a, int b)
{
    return a % b;
}
